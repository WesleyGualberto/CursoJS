/*
While - Estrutura de repeticao com teste logico no inicio.
*/

var contador = 1

while (contador <= 6) {
    console.log('Tudo bem?')
    contador++
}

/* 
do while - Estrutura de repeticao com teste logico no final.
*/

var contador = 1

do {
    console.log('Tudo bem?')
    contador++
} while (contador <= 6)
